/**
 *****************************************************************************
 ** \brief BGR 使能
 **
 ** 
 ** \retval Null                                      
 *****************************************************************************/
void Bgr_BgrEnable(void)
{    
    M0P_BGR->CR |= 0x1u;
    
    delay10us(2);
}

/**
 *****************************************************************************
 ** \brief BGR 禁止
 **
 ** 
 ** \retval Null                                     
 *****************************************************************************/
void Bgr_BgrDisable(void)
{
    M0P_BGR->CR &= 0x2u;
}

/**
 *****************************************************************************
 ** \brief BGR 温度传感器使能(需要先开启BGR)
 **
 ** 
 ** \retval Null                                      
 *****************************************************************************/
void Bgr_TempSensorEnable(void)
{
    M0P_BGR->CR |= 0x2u;
    
    delay10us(2);  
}

/**
 *****************************************************************************
 ** \brief BGR 温度传感器禁止
 **
 ** 
 ** \retval Null                                      
 *****************************************************************************/
void Bgr_TempSensorDisable(void)
{
    M0P_BGR->CR &= 0x1u;
}


